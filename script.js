function add(num1 , num2){
    if(typeof num1 == 'number' && typeof num2 == 'number')
        return num1 + num2;
    else
        return -1;
}

function substract(num1 , num2){
    if(typeof num1 == 'number' && typeof num2 == 'number')
        return num1 - num2;
    else
        return -1;
}

function multiply(num1 , num2){
    if(typeof num1 == 'number' && typeof num2 == 'number')
        return num1 * num2;
    else
        return -1;
}

function divide(num1 , num2){
    if(typeof num1 == 'number' && typeof num2 == 'number')
        return num1 / num2;
    else
        return -1;
}
